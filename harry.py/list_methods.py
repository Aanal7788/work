l1 = [1, 8, 7, 2, 21, 15]
print(l1)
l1.sort() #sort the list(small to big number)
print(l1)
l1.reverse() #reverse the list
print(l1)
l1.append(45) #add at the end of the list
print(l1)
l1.insert(2, 544) #inserts 544 at index 2
print(l1)
l1.pop(2) #remove element at index 2
l1.remove(21) #removes 21 from the list
print(l1)